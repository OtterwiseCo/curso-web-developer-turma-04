let myNumber = 102;

// Se ele é positivo, negativo, 0 ou maior que 100

if (myNumber > 0 && myNumber <= 100) {
  console.log("positivo");
} else if (myNumber === 0) {
  console.log("é zero");
} else if (myNumber < 0) {
  console.log("negativo");
} else if (myNumber > 100) {
  console.log("maior que 100");
}
